-- ══════════════════════════════════════════════════════════════════════════════
-- TRIGGER FOR AUTO-CREATING BOUNDARIES ON NEW USER SIGNUP
-- ══════════════════════════════════════════════════════════════════════════════
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
    INSERT INTO public.profiles (id, role, created_at)
    VALUES (
        NEW.id,
        'ionic_user',
        now()
    );

    -- Insert default autonomy boundaries
    INSERT INTO public.agent_autonomy_boundaries (user_id, action_type, zone)
    VALUES
        (NEW.id, 'create_goal', 'yellow'),
        (NEW.id, 'send_alert', 'green'),
        (NEW.id, 'update_task_status', 'yellow')
    ON CONFLICT (user_id, action_type) DO NOTHING;

    RETURN NEW;
END;
$$;

-- Seed default autonomy boundaries for existing users/profiles
INSERT INTO public.agent_autonomy_boundaries (user_id, action_type, zone)
SELECT p.id, d.action_type, d.zone
FROM public.profiles p
CROSS JOIN (
    VALUES
        ('create_goal', 'yellow'),
        ('send_alert', 'green'),
        ('update_task_status', 'yellow')
) AS d(action_type, zone)
ON CONFLICT (user_id, action_type) DO NOTHING;

-- Apply set_updated_at triggers for goals and tasks
CREATE TRIGGER trg_agent_goals_updated_at
    BEFORE UPDATE ON public.agent_goals
    FOR EACH ROW
    EXECUTE FUNCTION public.set_updated_at();

CREATE TRIGGER trg_agent_tasks_updated_at
    BEFORE UPDATE ON public.agent_tasks
    FOR EACH ROW
    EXECUTE FUNCTION public.set_updated_at();

