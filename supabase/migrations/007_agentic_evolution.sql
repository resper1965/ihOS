-- ──────────────────────────────────────────────────────────────────────────────
-- 1. AGENT_GOALS — GRC Remediation Projects
-- ──────────────────────────────────────────────────────────────────────────────
CREATE TABLE public.agent_goals (
    id             UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id        UUID        NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    framework_code VARCHAR     NOT NULL,
    title          TEXT        NOT NULL,
    description    TEXT        NULL,
    status         VARCHAR     NOT NULL DEFAULT 'not_started' CHECK (status IN ('not_started', 'in_progress', 'completed')),
    progress       INTEGER     NOT NULL DEFAULT 0 CHECK (progress >= 0 AND progress <= 100),
    created_at     TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at     TIMESTAMPTZ NOT NULL DEFAULT now()
);

COMMENT ON TABLE public.agent_goals IS 'GRC Remediation Projects / Goals for compliance framework gaps';
COMMENT ON COLUMN public.agent_goals.framework_code IS 'Framework code code (e.g. ISO_27001, SOC_2, NIST_800_53)';
COMMENT ON COLUMN public.agent_goals.status IS 'Status of the remediation goal';

CREATE INDEX idx_agent_goals_user ON public.agent_goals (user_id);
CREATE INDEX idx_agent_goals_framework ON public.agent_goals (framework_code);

-- ──────────────────────────────────────────────────────────────────────────────
-- 2. AGENT_TASKS — Technical Execution Tasks
-- ──────────────────────────────────────────────────────────────────────────────
CREATE TABLE public.agent_tasks (
    id             UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    goal_id        UUID        NOT NULL REFERENCES public.agent_goals(id) ON DELETE CASCADE,
    title          TEXT        NOT NULL,
    description    TEXT        NULL,
    status         VARCHAR     NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'in_progress', 'completed')),
    deadline       TIMESTAMPTZ NULL,
    assigned_agent VARCHAR     NULL,
    created_at     TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at     TIMESTAMPTZ NOT NULL DEFAULT now()
);

COMMENT ON TABLE public.agent_tasks IS 'Technical execution tasks mapped to specific GRC goals';
COMMENT ON COLUMN public.agent_tasks.status IS 'Task status: pending, in_progress, completed';

CREATE INDEX idx_agent_tasks_goal ON public.agent_tasks (goal_id);
CREATE INDEX idx_agent_tasks_deadline ON public.agent_tasks (deadline);

-- ──────────────────────────────────────────────────────────────────────────────
-- 3. AGENT_NOTIFICATIONS — Proactive Alerts
-- ──────────────────────────────────────────────────────────────────────────────
CREATE TABLE public.agent_notifications (
    id         UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id    UUID        NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    title      TEXT        NOT NULL,
    content    TEXT        NOT NULL,
    type       VARCHAR     NOT NULL CHECK (type IN ('poam_expiry', 'score_change', 'task_deadline')),
    read       BOOLEAN     NOT NULL DEFAULT false,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

COMMENT ON TABLE public.agent_notifications IS 'Logs of proactive alerts generated by agentic checks';
COMMENT ON COLUMN public.agent_notifications.type IS 'Category of alert: poam_expiry, score_change, task_deadline';

CREATE INDEX idx_agent_notifications_user_read ON public.agent_notifications (user_id, read);

-- ──────────────────────────────────────────────────────────────────────────────
-- 4. AGENT_LEARNING_CORRECTIONS — Feedback & Fine-tuning Loop
-- ──────────────────────────────────────────────────────────────────────────────
CREATE TABLE public.agent_learning_corrections (
    id                         UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id                    UUID        NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    conversation_id            UUID        NOT NULL REFERENCES public.conversations(id) ON DELETE CASCADE,
    message_id                 UUID        NULL REFERENCES public.messages(id) ON DELETE SET NULL,
    user_correction            TEXT        NOT NULL,
    agent_misaligned_response  TEXT        NOT NULL,
    learned_context            TEXT        NULL,
    created_at                 TIMESTAMPTZ NOT NULL DEFAULT now()
);

COMMENT ON TABLE public.agent_learning_corrections IS 'Stores alignment feedback/corrections provided by users to tune agent context';

CREATE INDEX idx_agent_corrections_user ON public.agent_learning_corrections (user_id);

-- ──────────────────────────────────────────────────────────────────────────────
-- 5. AGENT_AUTONOMY_BOUNDARIES — Autonomy Zones (Green/Yellow/Red)
-- ──────────────────────────────────────────────────────────────────────────────
CREATE TABLE public.agent_autonomy_boundaries (
    id          UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id     UUID        NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    action_type VARCHAR     NOT NULL,
    zone        VARCHAR     NOT NULL CHECK (zone IN ('green', 'yellow', 'red')),
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT uq_user_action UNIQUE (user_id, action_type)
);

COMMENT ON TABLE public.agent_autonomy_boundaries IS 'Autonomy zone configuration per action type for the agent';
COMMENT ON COLUMN public.agent_autonomy_boundaries.zone IS 'green: autonomous, yellow: requires approval, red: forbidden';

CREATE INDEX idx_agent_autonomy_user ON public.agent_autonomy_boundaries (user_id);

-- ──────────────────────────────────────────────────────────────────────────────
-- 6. AGENT_ORG_STATE — Dynamic Organizational Context
-- ──────────────────────────────────────────────────────────────────────────────
CREATE TABLE public.agent_org_state (
    id          UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id     UUID        NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    state_key   VARCHAR     NOT NULL,
    state_value JSONB       NOT NULL,
    updated_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT uq_user_state_key UNIQUE (user_id, state_key)
);

COMMENT ON TABLE public.agent_org_state IS 'Dynamic organizational data (e.g. risk levels, maturity score) for system prompts';

CREATE INDEX idx_agent_org_state_user ON public.agent_org_state (user_id);

